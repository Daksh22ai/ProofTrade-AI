# Multiple Mantle Sepolia RPCs tried in order — single RPC is a demo-killer
_MANTLE_SEPOLIA_RPCS = [
    os.getenv("MANTLE_SEPOLIA_RPC", ""),
    "https://rpc.sepolia.mantle.xyz",
    "https://mantle-sepolia.drpc.org",
    "https://endpoints.omniatech.io/v1/mantle/sepolia/public",
]


def _get_w3() -> Web3:
    """Return connected Web3 to Mantle Sepolia, trying fallback RPCs."""
    for rpc in _MANTLE_SEPOLIA_RPCS:
        if not rpc:
            continue
        try:
            w3 = Web3(Web3.HTTPProvider(rpc, request_kwargs={"timeout": 15}))
            w3.eth.block_number   # connectivity test
            return w3
        except Exception:
            continue
    raise ConnectionError("All Mantle Sepolia RPC endpoints failed")

