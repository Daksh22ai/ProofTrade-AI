"""
on_chain/submit_audit.py — Submit AI analysis hash to Mantle Sepolia AuditLog contract.

Hash is keccak256(deterministic JSON of the analysis output).
JSON uses sort_keys=True and no whitespace for reproducibility.
The hash commits to the exact verdict, confluence count, and pre-trade note
that was generated by the agents — proving it was made at a specific block timestamp.

Judges can verify: recompute the hash from the displayed analysis and compare to on-chain.
"""

